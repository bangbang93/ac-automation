fn unique_in_order<T>(sequence: T) -> Vec<T::Item>
where
    T: std::iter::IntoIterator,
    T::Item: std::cmp::PartialEq + std::fmt::Debug,
{
    let mut result = Vec::new();
    for item in sequence {
        if result.last() != Some(&item) {
            result.push(item);
        }
    }
    result
}

